public class PrintPrimitives {
	
	static int i;
	static char ch;
	static byte b;
	static boolean bool;
	static long l;
	static short sh;
	static double d;
	static float f;
	public static void main(String[] args) {
		System.out.println("Default values of Integer :"+i);
		System.out.println("Default values of Character :"+ch);
		System.out.println("Default values of Byte :"+b);
		System.out.println("Default values of Boolean :"+bool);
		System.out.println("Default values of Long :"+l);
		System.out.println("Default values of Double :"+d);
		System.out.println("Default values of Byte :"+f);
		System.out.println("Default values of Short :"+sh);
	
}

}
